import React, { useState } from 'react';
import { Dialog, DialogContent, DialogDescription, DialogHeader, DialogTitle } from './ui/dialog';
import { Button } from './ui/button';
import { Input } from './ui/input';
import { Label } from './ui/label';
import { Alert, AlertDescription } from './ui/alert';
import { Copy, CheckCircle, ExternalLink, Database, Key, AlertCircle } from 'lucide-react';
import { Card, CardContent } from './ui/card';

interface SupabaseSetupWizardProps {
  open: boolean;
  onClose: () => void;
}

export function SupabaseSetupWizard({ open, onClose }: SupabaseSetupWizardProps) {
  const [step, setStep] = useState(1);
  const [anonKey, setAnonKey] = useState('');
  const [copied, setCopied] = useState(false);

  const projectId = 'slwuctsdhqwdjwmyxsjn';
  const dashboardUrl = `https://supabase.com/dashboard/project/${projectId}/settings/api`;

  const handleCopyProjectId = () => {
    navigator.clipboard.writeText(projectId);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const isValidAnonKey = (key: string) => {
    return key.startsWith('eyJ') && key.length > 100;
  };

  const handleSaveKey = () => {
    if (!isValidAnonKey(anonKey)) {
      alert('Invalid anon key format. It should start with "eyJ" and be very long.');
      return;
    }

    // Show instructions for manual update
    const fileContent = `/* AUTOGENERATED FILE - DO NOT EDIT CONTENTS */

export const projectId = "${projectId}"
export const publicAnonKey = "${anonKey}"`;

    alert(`Please update /utils/supabase/info.tsx with this content:\n\n${fileContent}`);
    onClose();
  };

  return (
    <Dialog open={open} onOpenChange={(open) => !open && onClose()}>
      <DialogContent className="max-w-2xl max-h-[90vh] overflow-y-auto">
        <DialogHeader>
          <DialogTitle className="flex items-center gap-2">
            <Database className="w-5 h-5 text-pink-600" />
            Supabase Setup Wizard
          </DialogTitle>
          <DialogDescription>
            Step {step} of 3: Get your Supabase anon key
          </DialogDescription>
        </DialogHeader>

        <div className="space-y-6">
          {/* Step 1: Open Dashboard */}
          <Card className={step >= 1 ? 'border-pink-200 bg-pink-50' : 'border-gray-200'}>
            <CardContent className="pt-6">
              <div className="flex items-start gap-3">
                <div className="w-8 h-8 rounded-full bg-pink-600 text-white flex items-center justify-center flex-shrink-0 font-bold">
                  1
                </div>
                <div className="flex-1">
                  <h3 className="font-semibold mb-2">Open Supabase Dashboard</h3>
                  <p className="text-sm text-gray-600 mb-3">
                    Click the button below to open your project's API settings page
                  </p>
                  <Button
                    onClick={() => {
                      window.open(dashboardUrl, '_blank');
                      setStep(2);
                    }}
                    className="w-full bg-gradient-to-r from-pink-600 to-red-600"
                  >
                    <ExternalLink className="w-4 h-4 mr-2" />
                    Open Supabase API Settings
                  </Button>
                </div>
              </div>
            </CardContent>
          </Card>

          {/* Step 2: Find Anon Key */}
          {step >= 2 && (
            <Card className={step >= 2 ? 'border-pink-200 bg-pink-50' : 'border-gray-200'}>
              <CardContent className="pt-6">
                <div className="flex items-start gap-3">
                  <div className="w-8 h-8 rounded-full bg-pink-600 text-white flex items-center justify-center flex-shrink-0 font-bold">
                    2
                  </div>
                  <div className="flex-1">
                    <h3 className="font-semibold mb-2">Copy Your Anon Key</h3>
                    <div className="space-y-3 text-sm">
                      <p className="text-gray-600">In the Supabase dashboard, you'll see:</p>
                      
                      <div className="bg-white border rounded-lg p-4 space-y-2">
                        <p className="font-mono text-xs text-gray-500">Project API keys</p>
                        <div className="space-y-2">
                          <div className="flex items-center gap-2">
                            <Key className="w-4 h-4 text-green-600" />
                            <span className="font-semibold text-green-700">anon</span>
                            <span className="text-xs text-gray-500">public</span>
                          </div>
                          <div className="bg-gray-100 p-2 rounded font-mono text-xs break-all">
                            eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9...
                          </div>
                          <Button variant="outline" size="sm" className="w-full">
                            <Copy className="w-3 h-3 mr-1" />
                            Copy
                          </Button>
                        </div>
                      </div>

                      <Alert className="bg-yellow-50 border-yellow-200">
                        <AlertCircle className="h-4 w-4 text-yellow-600" />
                        <AlertDescription className="text-yellow-800 text-xs">
                          <strong>Important:</strong> Copy the <strong>"anon"</strong> key, NOT:
                          <ul className="list-disc list-inside mt-1 ml-2">
                            <li>❌ service_role key (secret)</li>
                            <li>❌ publishable key (for Stripe)</li>
                          </ul>
                        </AlertDescription>
                      </Alert>

                      <Button
                        onClick={() => setStep(3)}
                        variant="outline"
                        className="w-full"
                      >
                        I've Copied the Anon Key →
                      </Button>
                    </div>
                  </div>
                </div>
              </CardContent>
            </Card>
          )}

          {/* Step 3: Paste & Save */}
          {step >= 3 && (
            <Card className="border-pink-200 bg-pink-50">
              <CardContent className="pt-6">
                <div className="flex items-start gap-3">
                  <div className="w-8 h-8 rounded-full bg-pink-600 text-white flex items-center justify-center flex-shrink-0 font-bold">
                    3
                  </div>
                  <div className="flex-1 space-y-3">
                    <h3 className="font-semibold">Paste Your Anon Key</h3>
                    
                    <div className="space-y-2">
                      <Label htmlFor="anon-key">Supabase Anon Key</Label>
                      <Input
                        id="anon-key"
                        placeholder="eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9..."
                        value={anonKey}
                        onChange={(e) => setAnonKey(e.target.value)}
                        className="font-mono text-xs"
                      />
                      {anonKey && !isValidAnonKey(anonKey) && (
                        <p className="text-xs text-red-600 flex items-center gap-1">
                          <AlertCircle className="w-3 h-3" />
                          Invalid format. Anon key should start with "eyJ" and be very long.
                        </p>
                      )}
                      {anonKey && isValidAnonKey(anonKey) && (
                        <p className="text-xs text-green-600 flex items-center gap-1">
                          <CheckCircle className="w-3 h-3" />
                          Looks good! Format is correct.
                        </p>
                      )}
                    </div>

                    <Alert className="bg-blue-50 border-blue-200">
                      <AlertDescription className="text-blue-800 text-xs">
                        <strong>Next step:</strong> After clicking "Show Update Code", copy the code and update your <code className="bg-blue-100 px-1 rounded">/utils/supabase/info.tsx</code> file.
                      </AlertDescription>
                    </Alert>

                    <Button
                      onClick={handleSaveKey}
                      disabled={!isValidAnonKey(anonKey)}
                      className="w-full bg-gradient-to-r from-pink-600 to-red-600"
                    >
                      Show Update Code
                    </Button>
                  </div>
                </div>
              </CardContent>
            </Card>
          )}

          {/* Quick Reference */}
          <Card className="border-gray-200">
            <CardContent className="pt-4">
              <h4 className="font-semibold text-sm mb-2 flex items-center gap-2">
                <Database className="w-4 h-4" />
                Your Project Info
              </h4>
              <div className="space-y-2 text-xs">
                <div className="flex items-center justify-between bg-gray-50 p-2 rounded">
                  <span className="text-gray-600">Project ID:</span>
                  <div className="flex items-center gap-2">
                    <code className="bg-white px-2 py-1 rounded border">{projectId}</code>
                    <Button
                      variant="ghost"
                      size="sm"
                      onClick={handleCopyProjectId}
                      className="h-6 px-2"
                    >
                      {copied ? <CheckCircle className="w-3 h-3 text-green-600" /> : <Copy className="w-3 h-3" />}
                    </Button>
                  </div>
                </div>
                <div className="flex items-center justify-between bg-gray-50 p-2 rounded">
                  <span className="text-gray-600">Project URL:</span>
                  <code className="bg-white px-2 py-1 rounded border text-[10px]">
                    https://{projectId}.supabase.co
                  </code>
                </div>
              </div>
            </CardContent>
          </Card>
        </div>
      </DialogContent>
    </Dialog>
  );
}
